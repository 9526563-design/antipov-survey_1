@app.route('/survey/<step>', methods=['GET','POST'])
def survey_step(step):
    sid = session.get('sid')
    if not sid:
        return redirect(url_for('index'))
    sdata = get_survey_session(sid)
    if not sdata:
        return redirect(url_for('index'))
    group = sdata['group']
    answers = sdata.get('answers', {})

    if request.method == 'POST':
        form_data = request.form.to_dict(flat=False)
        # Сохраняем ответы с нормализацией ключей (otec1_5 -> otec_5)
        import re as _re
        for k, v in form_data.items():
            val = v[0] if len(v) == 1 else v
            m = _re.match('^([a-z]+)[0-9]+_([0-9]+)$', k)
            norm_k = (m.group(1) + '_' + m.group(2)) if m else k
            answers[norm_k] = val
        sdata['answers'] = answers
        # Обновляем текущий шаг
        idx = SURVEY_STEPS.index(step)
        next_step = SURVEY_STEPS[idx + 1]
        sdata['current_step'] = next_step if next_step != 'done' else step
        set_survey_session(sid, sdata)
        save_session_to_disk(sid, sdata)

        if next_step == 'done':
            save_response(group, answers)
            # Сохраняем answers в сессии для страницы результатов
            result_sid = str(uuid.uuid4())
            set_survey_session(result_sid, {'answers': answers, 'group': group, 'type': 'results'})
            session['result_sid'] = result_sid
            clear_survey_session(sid)
            return redirect(url_for('results'))
        # После каждой методики показываем экран с кодом и кнопкой "продолжить"
        resume_code = sdata.get('resume_code', '')
        return redirect(url_for('step_complete', step=step, next_step=next_step))

    # GET — показываем форму
    idx = SURVEY_STEPS.index(step)
    progress = int((idx / (len(SURVEY_STEPS) - 1)) * 100)

    if step == 'socdem':
        fields = SOCDEM_ENTREPRENEUR if group == 'entrepreneur' else SOCDEM_EMPLOYEE
        group_label = 'Предприниматель' if group == 'entrepreneur' else 'Наёмный работник'
        return render_template('socdem.html', fields=fields, group_label=group_label,
                               progress=progress, step=step, step_num=idx+1, total_steps=len(SURVEY_STEPS)-1)
    elif step in METHOD_PARTS:
        parent, start, end = METHOD_PARTS[step]
        base_method = METHODS[parent].copy()
        # Для pfq — берём срез секций
        if parent == 'pfq':
            base_method = dict(base_method)
            base_method['sections'] = base_method['sections'][start:end]
        else:
            # Для остальных — срез questions
            base_method = dict(base_method)
            base_method['questions'] = base_method['questions'][start:end]
        # Добавляем номер части в заголовок
        part_num = int(step[-1])
        total_parts = sum(1 for k in METHOD_PARTS if METHOD_PARTS[k][0] == parent)
        base_method['title'] = base_method['title'] + f' (часть {part_num}/{total_parts})'
        return render_template('method.html', method=base_method, step=step,
                               progress=progress, step_num=idx+1, total_steps=len(SURVEY_STEPS)-1,
                               next_step=SURVEY_STEPS[idx+1], group=group)
    elif step in METHODS:
        method = METHODS[step]
        return render_template('method.html', method=method, step=step,
                               progress=progress, step_num=idx+1, total_steps=len(SURVEY_STEPS)-1,
                               next_step=SURVEY_STEPS[idx+1],
                               group=group)
    return redirect(url_for('done'))

