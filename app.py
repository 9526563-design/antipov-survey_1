def safe_int(val, default=0):
    try: return int(val)
    except: return default

def compute_scales(data, group_type):
    """Подсчитывает шкальные баллы из сырых ответов"""
    s = {}

    # Группа: 1=предприниматель, 2=наёмный
    s['group_num'] = 1 if group_type == 'entrepreneur' else 2

    # ── 5PFQ ──────────────────────────────────────────────
    # Каждый пункт: значение от -2 до +2, сумма по 15 пунктам каждого фактора
    # Фактор IV (Эмоц.неустойчивость, пункты 46-60): пункты прямые (высокий балл = высокий нейротизм)
    for factor, name, items in [
        ('pfq_e', 'Экстраверсия',         list(range(1,16))),
        ('pfq_a', 'Привязанность',         list(range(16,31))),
        ('pfq_c', 'Самоконтроль',          list(range(31,46))),
        ('pfq_n', 'Нейротизм',             list(range(46,61))),
        ('pfq_o', 'Экспрессивность',       list(range(61,76))),
    ]:
        vals = [safe_int(data.get(f'pfq_{i}', 0)) for i in items]
        s[factor] = sum(vals)

    # ── УСК ───────────────────────────────────────────────
    # Прямые (ДА=1): 2,4,11,12,13,15,16,17,19,20,22,25,27,29,31,32,34,36,37,39,42,44
    # Обратные (НЕТ=1): 1,3,5,6,7,8,9,10,14,18,21,23,24,26,28,30,33,35,38,40,41,43
    usk_direct = {2,4,11,12,13,15,16,17,19,20,22,25,27,29,31,32,34,36,37,39,42,44}
    usk_reverse = {1,3,5,6,7,8,9,10,14,18,21,23,24,26,28,30,33,35,38,40,41,43}
    # Субшкалы УСК
    usk_scales = {
        'usk_io': list(range(1,45)),  # Общий
        'usk_id': [12,15,27,32,36,37],  # Достижения
        'usk_in': [1,5,6,7,14,24,33,40,43],  # Неудачи
        'usk_is': [2,16,20,32,37,41],  # Семья
        'usk_ip': [9,10,19,21,22,31,42,44],  # Производство
        'usk_im': [4,6,15,27,32,38],  # Межличностные
        'usk_iz': [3,13,23,34],  # Здоровье
    }
    for scale_name, items in usk_scales.items():
        total = 0
        for i in items:
            raw = safe_int(data.get(f'usk_{i}', 0))
            if i in usk_direct:
                total += raw  # ДА=1, НЕТ=0
            else:
                total += (1 - raw)  # НЕТ=1, ДА=0
        s[scale_name] = total

    # ── ОТеЦ ──────────────────────────────────────────────
    # 8 ценностей × 5 сфер. Ключ: каждые 8 пунктов = одна сфера, каждый пункт относится к одной ценности
    # Ценности: СД=саморазвитие, ДУ=духовное удовлетворение, КД=креативность, СР=социальные результаты
    #           СО=социальные контакты, ПР=престиж, ВЛ=высокое материальное положение, УО=достижения
    # Распределение пунктов по ценностям (1-80):
    otec_value_map = {
        'otec_sd': [1,9,19,22,34,39,47,55,63,73,77],   # Саморазвитие
        'otec_du': [2,12,18,20,37,44,53,58,66,75],      # Духовное удовлетворение
        'otec_kr': [5,25,26,31,45,60,65,71],             # Креативность
        'otec_sr': [3,10,16,21,36,57,79],                # Социальные результаты
        'otec_so': [4,13,18,24,44,51,64,78],             # Социальные контакты
        'otec_pr': [1,9,34,39,47,55,63,77],              # Престиж
        'otec_vl': [6,23,29,30,48,61,67],                # Высокое материальное положение
        'otec_uo': [7,8,11,14,15,17,27,28,32,33,35,38,40,41,42,43,46,49,50,52,54,56,59,62,68,69,70,72,74,76,80],  # Достижения
    }
    for scale_name, items in otec_value_map.items():
        s[scale_name] = sum(safe_int(data.get(f'otec_{i}', 0)) for i in items)

    # ── MSTAT-I ───────────────────────────────────────────
    # Обратные пункты: 1,3,5,8,10,11,12,13,15,17,19
    mstat_reverse = {1,3,5,8,10,11,12,13,15,17,19}
    total = 0
    for i in range(1,23):
        val = safe_int(data.get(f'mstat_{i}', 3))
        if i in mstat_reverse:
            val = 6 - val  # инверсия: 1→5, 2→4, 3→3, 4→2, 5→1
        total += val
    s['mstat_total'] = total

    # ── ЛФР-25 ────────────────────────────────────────────
    # Рациональность: пункты 1,2,5,6,8,9,10,11,12,14,19,24 — все прямые
    # Риск: пункты 3,4(R),7,13,15,16,17,18,20(R),21,22,23,25
    lfr_rational = [1,2,5,6,8,9,10,11,12,14,19,24]
    lfr_risk = [3,4,7,13,15,16,17,18,20,21,22,23,25]
    lfr_risk_reverse = {4,20}
    s['lfr_r'] = sum(safe_int(data.get(f'lfr_{i}', 0)) for i in lfr_rational)
    risk_total = 0
    for i in lfr_risk:
        val = safe_int(data.get(f'lfr_{i}', 3))
        if i in lfr_risk_reverse:
            val = 6 - val
        risk_total += val
    s['lfr_risk'] = risk_total

    # ── Шкала лжи ─────────────────────────────────────────
    # «Честные» ответы (указывают на социальную желательность):
    # ДА: 1,2,5,8,10,13,16,17,19,21 / НЕТ: 3,4,6,7,9,11,12,14,15,18,20
    lie_yes = {1,2,5,8,10,13,16,17,19,21}
    lie_no  = {3,4,6,7,9,11,12,14,15,18,20}
    lie_score = 0
    for i in range(1,22):
        val = safe_int(data.get(f'lie_{i}', 0))
        if i in lie_yes and val == 1: lie_score += 1
        if i in lie_no  and val == 0: lie_score += 1
    s['lie_total'] = lie_score
    s['lie_valid'] = 1 if lie_score < 5 else 0  # 1=достоверный, 0=недостоверный

    # ── MATS ──────────────────────────────────────────────
    # A=мотивация успеха (1 балл), B=избегание неудачи (0 баллов)
    # Итоговый балл = количество выборов A (чем выше — тем сильнее мотивация успеха)
    mats_a = sum(1 for i in range(1,33) if data.get(f'mats_{i}','') == 'A')
    mats_b = 32 - mats_a
    s['mats_success'] = mats_a    # мотивация успеха
    s['mats_avoid']   = mats_b    # избегание неудачи
    s['mats_total']   = mats_a - mats_b  # общий индекс (-32 до +32)

    # ── ЭмИн ──────────────────────────────────────────────
    # Субшкалы:
    # МП (понимание чужих эмоций): 1,3,8,13,14,19,20,25,29,33,36,40,41,42,45,46
    # МУ (управление чужими эмоциями): 2,4,15,16,18,30,32
    # ВП (понимание своих эмоций): 5,7,9,12,17,22,26,31,37
    # ВУ (управление своими эмоциями): 6,10,11,21,23,24,27,28,34,35,38,39,43,44
    # Обратные: 2,5,7,9,17,25,29,34,38,39
    emin_reverse = {2,5,7,9,17,25,29,34,38,39}
    emin_mp = [1,3,8,13,14,19,20,25,29,33,36,40,41,42,45,46]
    emin_mu = [2,4,15,16,18,30,32]
    emin_vp = [5,7,9,12,17,22,26,31,37]
    emin_vu = [6,10,11,21,23,24,27,28,34,35,38,39,43,44]

    def emin_val(i):
        v = safe_int(data.get(f'emin_{i}', 0))
        return (5 - v) if i in emin_reverse else v

    s['emin_mp'] = sum(emin_val(i) for i in emin_mp)
    s['emin_mu'] = sum(emin_val(i) for i in emin_mu)
    s['emin_vp'] = sum(emin_val(i) for i in emin_vp)
    s['emin_vu'] = sum(emin_val(i) for i in emin_vu)
    s['emin_mei'] = s['emin_mp'] + s['emin_mu']  # Межличностный ЭИ
    s['emin_vei'] = s['emin_vp'] + s['emin_vu']  # Внутриличностный ЭИ
    s['emin_total'] = s['emin_mei'] + s['emin_vei']

    # ── Самоэффективность ─────────────────────────────────
    # Все 10 пунктов прямые, сумма (10-40)
    s['gses_total'] = sum(safe_int(data.get(f'gses_{i}', 0)) for i in range(1,11))

    # ── РОКО ──────────────────────────────────────────────
    # Среднее по каждой из 3 ориентаций (17 ситуаций)
    roko_a_vals = [safe_int(data.get(f'roko_{i}_a', 0)) for i in range(1,18)]
    roko_p_vals = [safe_int(data.get(f'roko_{i}_p', 0)) for i in range(1,18)]
    roko_b_vals = [safe_int(data.get(f'roko_{i}_b', 0)) for i in range(1,18)]
    s['roko_a'] = round(sum(roko_a_vals) / 17, 2)  # Автономная
    s['roko_p'] = round(sum(roko_p_vals) / 17, 2)  # Подконтрольная
    s['roko_b'] = round(sum(roko_b_vals) / 17, 2)  # Безличная

    return s

