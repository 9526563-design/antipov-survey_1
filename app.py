# Серверное хранилище активных сессий опроса
# Решает проблему переполнения cookie при большом количестве ответов
_sessions = {}
_sessions_lock = threading.Lock()

def get_survey_session(sid):
    with _sessions_lock:
        return _sessions.get(sid, {}).copy()

def set_survey_session(sid, data):
    with _sessions_lock:
        _sessions[sid] = data

def clear_survey_session(sid):
    with _sessions_lock:
        _sessions.pop(sid, None)

def generate_resume_code():
    """Генерирует читаемый 8-символьный код возобновления (без похожих символов)"""
    chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789'
    return ''.join(random.choices(chars, k=4)) + '-' + ''.join(random.choices(chars, k=4))

def save_session_to_disk(sid, sdata):
    """Сохраняет активную сессию на диск по коду возобновления"""
    code = sdata.get('resume_code')
    if not code:
        return
    paused_file = os.path.join(os.path.dirname(__file__), 'paused_sessions.json')
    _lock2 = threading.Lock()
    try:
        if os.path.exists(paused_file):
            with open(paused_file, 'r', encoding='utf-8') as f:
                paused = json.load(f)
        else:
            paused = {}
        paused[code] = {
            'sid': sid,
            'group': sdata.get('group'),
            'step': sdata.get('current_step', 'socdem'),
            'answers': sdata.get('answers', {}),
            'saved_at': datetime.now().strftime('%Y-%m-%d %H:%M:%S')
        }
        with open(paused_file, 'w', encoding='utf-8') as f:
            json.dump(paused, f, ensure_ascii=False, indent=2)
    except Exception:
        pass  # на Render диск может быть readonly — код всё равно работает через memory

def load_session_by_code(code):
    """Загружает сохранённую сессию по коду возобновления"""
    code = code.upper().strip()
    # Сначала ищем в памяти
    with _sessions_lock:
        for sid, sdata in _sessions.items():
            if sdata.get('resume_code') == code:
                return sid, sdata.copy()
    # Затем на диске
    paused_file = os.path.join(os.path.dirname(__file__), 'paused_sessions.json')
    if os.path.exists(paused_file):
        try:
            with open(paused_file, 'r', encoding='utf-8') as f:
                paused = json.load(f)
            if code in paused:
                data = paused[code]
                new_sid = str(uuid.uuid4())
                sdata = {
                    'group': data['group'],
                    'answers': data.get('answers', {}),
                    'resume_code': code,
                    'current_step': data.get('step', 'socdem')
                }
                set_survey_session(new_sid, sdata)
                return new_sid, sdata
        except Exception:
            pass
    return None, None

