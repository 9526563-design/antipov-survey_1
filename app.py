DATA_FILE = os.path.join(os.path.dirname(__file__), 'responses.json')
_lock = threading.Lock()
_responses = []  # список dict: {id, group_type, timestamp, data}
_next_id = 1

def _load_from_disk():
    global _responses, _next_id
    if os.path.exists(DATA_FILE):
        try:
            with open(DATA_FILE, 'r', encoding='utf-8') as f:
                _responses = json.load(f)
            _next_id = max((r['id'] for r in _responses), default=0) + 1
        except Exception:
            _responses = []
            _next_id = 1

def _save_to_disk():
    try:
        with open(DATA_FILE, 'w', encoding='utf-8') as f:
            json.dump(_responses, f, ensure_ascii=False, indent=2)
    except Exception:
        pass  # на Render диск может быть readonly — данные останутся в памяти

def init_db():
    _load_from_disk()

def save_response(group_type, data):
    global _next_id
    with _lock:
        record = {
            'id': _next_id,
            'group_type': group_type,
            'timestamp': datetime.now().strftime('%Y-%m-%d %H:%M:%S'),
            'data': data
        }
        _responses.append(record)
        _next_id += 1
        _save_to_disk()

def get_all_responses():
    with _lock:
        return [(r['id'], r['group_type'], r['timestamp'], json.dumps(r['data'], ensure_ascii=False))
                for r in _responses]

def delete_response(rid):
    global _responses
    with _lock:
        _responses = [r for r in _responses if r['id'] != rid]
        _save_to_disk()

