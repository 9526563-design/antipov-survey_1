def safe_int(val, default=0):
    try: return int(val)
    except: return default

def compute_scales(data, group_type):
    """Подсчитывает шкальные баллы из сырых ответов"""
    # Нормализуем ключи: otec1_5 -> otec_5, izhs2_33 -> izhs_33 и т.д.
    import re
    normalized = {}
    for k, v in data.items():
        # Паттерн: методика + цифра + _ + номер (например otec1_5, izhs3_44, usk2_22)
        m = re.match('^([a-z]+)[0-9]+_([0-9]+)$', k)
        if m:
            normalized[m.group(1) + '_' + m.group(2)] = v
        else:
            normalized[k] = v
    data = normalized
    s = {}

    # Группа: 1=предприниматель, 2=наёмный
    s['group_num'] = 1 if group_type == 'entrepreneur' else 2

    # ── 5PFQ ──────────────────────────────────────────────
    # Каждый пункт: значение от -2 до +2, сумма по 15 пунктам каждого фактора
    # ── 5PFQ ──────────────────────────────────────────────
    # Ключ: Хромов А.Б. (2000). Шкала -2..+2, кодировка: -2→5, -1→4, 0→3, +1→2, +2→1
    # Распределение пунктов по факторам (каждые 5 пунктов по строкам бланка):
    # I  Экстраверсия:    1,6,11,16,21,26,31,36,41,46,51,56,61,66,71
    # II Привязанность:   2,7,12,17,22,27,32,37,42,47,52,57,62,67,72
    # III Самоконтроль:   3,8,13,18,23,28,33,38,43,48,53,58,63,68,73
    # IV Нейротизм:       4,9,14,19,24,29,34,39,44,49,54,59,64,69,74
    # V  Экспрессивность: 5,10,15,20,25,30,35,40,45,50,55,60,65,70,75
    pfq_factors = {
        'pfq_e': [1,6,11,16,21,26,31,36,41,46,51,56,61,66,71],
        'pfq_a': [2,7,12,17,22,27,32,37,42,47,52,57,62,67,72],
        'pfq_c': [3,8,13,18,23,28,33,38,43,48,53,58,63,68,73],
        'pfq_n': [4,9,14,19,24,29,34,39,44,49,54,59,64,69,74],
        'pfq_o': [5,10,15,20,25,30,35,40,45,50,55,60,65,70,75],
    }
    for factor, items in pfq_factors.items():
        total = 0
        for i in items:
            raw = safe_int(data.get(f'pfq_{i}', 0))  # -2..+2
            coded = {-2:5, -1:4, 0:3, 1:2, 2:1}.get(raw, 3)
            total += coded
        s[factor] = total

    # ── УСК ───────────────────────────────────────────────
    # Ключ: Бажин Е.Ф., Голынкина Е.А., Эткинд А.М.
    # Источник: Психологический журнал, 1984, Том 5, №3, с.161

    def usk_score(plus_items, minus_items):
        total = sum(safe_int(data.get(f'usk_{i}', 0)) for i in plus_items)
        total += sum(1 - safe_int(data.get(f'usk_{i}', 0)) for i in minus_items)
        return total

    # Ио — Общая интернальность (22+ / 22- = 44 пункта)
    s['usk_io'] = usk_score(
        [2,4,11,12,13,15,16,17,19,20,22,25,27,29,31,32,34,36,37,39,42,44],
        [1,3,5,6,7,8,9,10,14,18,21,23,24,26,28,30,33,35,38,40,41,43]
    )
    # Ид — Достижения (6+ / 6- = 12 пунктов)
    s['usk_id'] = usk_score([12,15,27,32,36,37], [1,5,6,14,26,43])
    # Ин — Неудачи (6+ / 6- = 12 пунктов)
    s['usk_in'] = usk_score([2,4,20,31,42,44], [7,24,33,38,40,41])
    # Ис — Семья (5+ / 5- = 10 пунктов)
    s['usk_is'] = usk_score([2,16,20,32,37], [7,14,26,28,41])
    # Ип — Производство (4+ / 4- = 8 пунктов)
    s['usk_ip'] = usk_score([19,22,25,42], [1,9,10,30])
    # Им — Межличностные (2+ / 2- = 4 пункта)
    s['usk_im'] = usk_score([4,27], [6,38])
    # Из — Здоровье (2+ / 2- = 4 пункта)
    s['usk_iz'] = usk_score([13,34], [3,23])

    # ── ОТеЦ ──────────────────────────────────────────────
    # Ключ: Сенин И.Г. Опросник терминальных ценностей. ЯрГУ, 1992.
    # 8 ценностей × 5 сфер жизни = 80 пунктов, каждый пункт — 2 на ячейку
    otec_value_map = {
        'otec_pr':  [5,6,7,21,27,49,57,65,73,74],   # Собственный престиж
        'otec_vl':  [8,11,29,30,36,45,58,59,63,66],  # Высокое материальное положение
        'otec_kr':  [2,9,25,31,37,48,53,55,72,79],   # Креативность
        'otec_so':  [15,16,22,28,34,42,47,54,60,64], # Активные социальные контакты
        'otec_sd':  [17,23,35,38,41,44,56,69,70,80], # Развитие себя
        'otec_do':  [1,10,13,24,32,46,50,52,68,76],  # Достижения
        'otec_du':  [3,4,18,33,39,40,43,62,71,78],   # Духовное удовлетворение
        'otec_in':  [12,14,19,26,29,51,61,67,75,77], # Сохранение индивидуальности
    }
    for scale_name, items in otec_value_map.items():
        s[scale_name] = sum(safe_int(data.get(f'otec_{i}', 0)) for i in items)

    # ── MSTAT-I ───────────────────────────────────────────
    # Ключ: Луковицкая Е.Г., 1998. Шкала 1-7.
    # Прямые пункты (высокий балл = высокая ТН): 4,7,11,12,14,15,17,18,19,21,22
    # Обратные пункты (инвертируются: 8-val): 1,2,3,5,6,8,9,10,13,16,20
    # Итог = сумма всех 22 пунктов (обратные инвертированы). Диапазон: 22–154
    # Нормы Луковицкой (n=130): M=95, SD=19.1
    mstat_direct  = {4,7,11,12,14,15,17,18,19,21,22}
    mstat_reverse = {1,2,3,5,6,8,9,10,13,16,20}
    mstat_answered = sum(1 for i in range(1,23) if data.get(f'mstat_{i}') is not None and data.get(f'mstat_{i}') != '')
    if mstat_answered == 0:
        s['mstat_total'] = None
    else:
        mstat_sum = 0
        for i in range(1,23):
            val = safe_int(data.get(f'mstat_{i}', 4))
            if i in mstat_reverse:
                val = 8 - val   # инверсия: 1→7, 7→1
            mstat_sum += val
        s['mstat_total'] = mstat_sum

    # ── ЛФР-25 ────────────────────────────────────────────
    # Ключ по Корниловой Т.В. (1994). Оригинальная шкала: −1 / 0 / +1.
    # В нашем приложении шкала 1–4 (безусловно нет → безусловно да).
    # Рациональность: пункты 1,2,5,6,8,9,10,11,12,14,19,24 — все прямые (12 пунктов)
    # Готовность к риску: пункты 3,4,13,15,16,17,18,21,22,23,25 — прямые (11 пунктов)
    #                     пункты 7,20 — обратные (инверсия: 5−val)
    # Источник: скан оригинального ключа из Корниловой Т.В. (1994)
    lfr_rational     = [1,2,5,6,8,9,10,11,12,14,19,24]
    lfr_risk_direct  = [3,4,13,15,16,17,18,21,22,23,25]
    lfr_risk_reverse = {7, 20}
    s['lfr_r'] = sum(safe_int(data.get(f'lfr_{i}', 2)) for i in lfr_rational)
    risk_total = 0
    for i in lfr_risk_direct:
        risk_total += safe_int(data.get(f'lfr_{i}', 2))
    for i in lfr_risk_reverse:
        risk_total += (5 - safe_int(data.get(f'lfr_{i}', 2)))  # инверсия: 1→4, 2→3, 3→2, 4→1
    s['lfr_risk'] = risk_total

    # ── Шкала лжи ─────────────────────────────────────────
    # «Честные» ответы (указывают на социальную желательность):
    # ДА: 1,2,5,8,10,13,16,17,19,21 / НЕТ: 3,4,6,7,9,11,12,14,15,18,20
    lie_yes = {1,2,5,8,10,13,16,17,19,21}
    lie_no  = {3,4,6,7,9,11,12,14,15,18,20}
    lie_score = 0
    for i in range(1,22):
        val = safe_int(data.get(f'lie_{i}', 0))
        if i in lie_yes and val == 1: lie_score += 1
        if i in lie_no  and val == 0: lie_score += 1
    s['lie_total'] = lie_score
    s['lie_valid'] = 1 if lie_score < 5 else 0  # 1=достоверный, 0=недостоверный

    # ── MATS (ТМД) ────────────────────────────────────────
    # Ключ: Магомед-Эминов М.Ш. (1988), форма А. Шкала ответов: -3..+3
    # Кодирование: -3→1, -2→2, -1→3, 0→4, +1→5, +2→6, +3→7
    # Прямые пункты (7 при +3 = мотивация успеха): 1,3,5,7,8,10,13,14,17,19,21,24,28,31
    # Обратные пункты (1 при +3 = избегание неудачи): 2,4,6,9,11,12,15,16,18,20,22,23,25,26,27,29,30,32
    mats_direct  = {1,3,5,7,8,10,13,14,17,19,21,24,28,31}
    mats_reverse = {2,4,6,9,11,12,15,16,18,20,22,23,25,26,27,29,30,32}
    mats_answered = sum(1 for i in range(1,33) if data.get(f'mats_{i}') is not None and data.get(f'mats_{i}') != '')
    if mats_answered == 0:
        s['mats_total'] = None
    else:
        mats_total = 0
        for i in range(1, 33):
            raw = safe_int(data.get(f'mats_{i}', 0))   # -3..+3
            coded = raw + 4   # перевод: -3→1 … +3→7
            if i in mats_reverse:
                coded = 8 - coded   # инверсия: 1→7, 7→1
            mats_total += coded
        s['mats_total'] = mats_total   # диапазон 32–224; >165=успех, <76=избегание

    # ── ЭмИн ──────────────────────────────────────────────
    # Ключ: Люсин Д.В. Опросник ЭмИн: новые психометрические данные, 2009
    # Субшкалы и знаки по Приложению (с. 276-277 PDF):
    # мп+: 1,3,11,13,20,27,29,32,34   мп-: 38,42,46
    # му+: 9,15,17,24,36              му-: 2,5,30,40,44
    # вп+: 7,14,26                    вп-: 8,18,22,31,35,41,45
    # ву+: 4,25,28,37                 ву-: 12,33,43
    # вэ+: 19,21,23                   вэ-: 6,10,16,39
    # Кодирование: прямые «совсем не согласен»=0…«полностью согласен»=3
    #              обратные — инверсия: 3-v
    emin_reverse = {2,5,6,8,10,12,16,18,22,30,31,33,35,38,39,40,41,42,43,44,45,46}

    def emin_val(i):
        v = safe_int(data.get(f'emin_{i}', 0))
        return (3 - v) if i in emin_reverse else v

    # МП — понимание чужих эмоций (12 пунктов): 1,3,11,13,20,27,29,32,34,38,42,46
    s['emin_mp'] = sum(emin_val(i) for i in [1,3,11,13,20,27,29,32,34,38,42,46])
    # МУ — управление чужими эмоциями (10 пунктов): 2,5,9,15,17,24,30,36,40,44
    s['emin_mu'] = sum(emin_val(i) for i in [2,5,9,15,17,24,30,36,40,44])
    # ВП — понимание своих эмоций (10 пунктов): 7,8,14,18,22,26,31,35,41,45
    s['emin_vp'] = sum(emin_val(i) for i in [7,8,14,18,22,26,31,35,41,45])
    # ВУ — управление своими эмоциями (7 пунктов): 4,12,25,28,33,37,43
    s['emin_vu'] = sum(emin_val(i) for i in [4,12,25,28,33,37,43])
    # ВЭ — контроль экспрессии (7 пунктов): 6,10,16,19,21,23,39
    s['emin_ve'] = sum(emin_val(i) for i in [6,10,16,19,21,23,39])
    # Составные шкалы
    s['emin_mei'] = s['emin_mp'] + s['emin_mu']           # МЭИ: межличностный ЭИ (22 пункта)
    s['emin_vei'] = s['emin_vp'] + s['emin_vu'] + s['emin_ve']  # ВЭИ: внутриличностный ЭИ (24 пункта)
    s['emin_total'] = s['emin_mei'] + s['emin_vei']        # Общий ЭИ

    # ── Самоэффективность ─────────────────────────────────
    # Все 10 пунктов прямые, сумма (10-40)
    s['gses_total'] = sum(safe_int(data.get(f'gses_{i}', 0)) for i in range(1,11))

    # ── ИЖС ───────────────────────────────────────────────
    def izhs_scale(items):
        return sum(1 for i in items if safe_int(data.get(f'izhs_{i}', 0)) == 1)
    # Ключ: Романова Е.С., Гребенников Л.Р. Механизмы психологической защиты. Мытищи, 1996.
    s['izhs_denial']     = izhs_scale([1,16,22,28,34,42,51,61,68,77,82,90,94])      # Отрицание (13)
    s['izhs_repression'] = izhs_scale([6,11,19,25,35,43,49,59,66,75,85,89])          # Подавление (12)
    s['izhs_regression'] = izhs_scale([2,14,18,26,33,48,50,58,69,78,86,88,93,95])   # Регрессия (14)
    s['izhs_comp']       = izhs_scale([3,10,24,29,37,45,52,64,65,74])                # Компенсация (10)
    s['izhs_proj']       = izhs_scale([7,9,23,27,38,41,55,63,71,73,84,92,96])        # Проекция (13)
    s['izhs_repl']       = izhs_scale([8,15,20,31,40,47,54,60,67,76,83,91,97])       # Замещение (13)
    s['izhs_intel']      = izhs_scale([4,13,17,30,36,44,56,62,70,80,81,87])          # Интеллектуализация (12)
    s['izhs_react']      = izhs_scale([5,12,21,32,39,46,53,57,72,79])                # Реактивное образование (10)
    s['izhs_total']      = sum([s['izhs_denial'], s['izhs_repression'], s['izhs_regression'],
                                s['izhs_comp'], s['izhs_proj'], s['izhs_repl'],
                                s['izhs_intel'], s['izhs_react']])

    # ── РОКО ──────────────────────────────────────────────
    # Ключ: Леонтьев Д.А., Дергачева О.Е. (2008). 26 ситуаций, выбор 1 из 3.
    # Каждый ответ кодируется как a=автономная, p=внешняя, b=безличная.
    # Итог = количество ответов каждого типа из 26 (сумма = 26).
    roko_a = sum(1 for i in range(1,27) if data.get(f'roko_{i}') == 'a')
    roko_p = sum(1 for i in range(1,27) if data.get(f'roko_{i}') == 'p')
    roko_b = sum(1 for i in range(1,27) if data.get(f'roko_{i}') == 'b')
    s['roko_a'] = roko_a   # Автономная ориентация (0–26)
    s['roko_p'] = roko_p   # Внешняя / подконтрольная (0–26)
    s['roko_b'] = roko_b   # Безличная (0–26)

    return s

